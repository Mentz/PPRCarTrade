    private void btn_CancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CancelarActionPerformed
        // TODO add your handling code here:
        
        for(ImageIcon ic : auxImagensRemov){
            this.imagens.add(ic);
        }
        
        for(ImageIcon ic : auxImagens){
            if(this.imagens.contains(ic)){
                System.out.println("Bah");
                this.imagens.remove(ic);
            }
        }
        this.dispose();
    }//GEN-LAST:event_btn_CancelarActionPerformed

    private void btn_AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AdicionarActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        FileFilter imageFilter = new FileNameExtensionFilter("Image files", ImageIO.getReaderFileSuffixes());
        fc.setFileFilter(imageFilter);
        int result = fc.showOpenDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            try {
                String path = fc.getSelectedFile().getAbsolutePath();
                imagens.add(new ImageIcon(path));
                if(!auxImagens.contains(imagens.get(imagens.size() - 1))){
                    auxImagens.add(imagens.get(imagens.size() - 1));
                }
                JOptionPane.showMessageDialog(this, "Imagem carregada com sucesso!");
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao carregar a imagem!", "Erro", JOptionPane.ERROR_MESSAGE);
            }            
        }
        jsl_Slider.setMaximum(Math.max(0, imagens.size() - 1));
    }//GEN-LAST:event_btn_AdicionarActionPerformed

    private void btn_RemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RemoverActionPerformed
        // TODO add your handling code here:
        if(imagens.size() > 0){
            int idx = Math.round(jsl_Slider.getValue());
            if(!auxImagensRemov.contains(imagens.get(idx))){
                auxImagensRemov.add(imagens.get(idx));
            }
            imagens.remove(idx);
            if(imagens.size() == 0){
                jlb_Image.setIcon(null);
            }
            JOptionPane.showMessageDialog(this, "Imagem removido com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Este veiculo n√£o possui imagens cadastradas!");
        }
        jsl_Slider.setMaximum(Math.max(0, imagens.size() - 1));
    }//GEN-LAST:event_btn_RemoverActionPerformed

